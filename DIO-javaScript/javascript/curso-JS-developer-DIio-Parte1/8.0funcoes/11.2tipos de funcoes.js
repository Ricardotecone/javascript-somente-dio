//funções declatativas
function funcao(){
    console.log("sou uma função")
}

funcao()

//expressões de função:
//funções atribuidas as expressões.
//a nomeação das funções por expressão
//é opcional posso colocar nome ou não exemplo:

var funcao = function nomeDaFuncao(){
    console.log("sou uma função com nome é opcional")
    // ${intrucao}
}
funcao()


var funcao = function(){
    console.log("sou uma função sem nome é opcional")
    // ${intrucao}
}
funcao()


//Arrow function
//expressão de sintaxe curta
//não podem ser nomeadas, sempre serão anônimas

//exemplo 1
var funcao = () => {
    console.log("sou uma Arrow function")
    // ${instrucao}
}
funcao()


